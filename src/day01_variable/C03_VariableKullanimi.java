package day01_variable;

public class C03_VariableKullanimi {


    public static void main(String[] args) {

        // Kullanıcının hesap bakiyesini tutan bir variable olusturun

        double bakiye= 0;

        //Kullanıcının hesap bakiyesini 100 artırın

        bakiye= bakiye +100;


        System.out.println("17. satirdaki bakiye  :"+ bakiye); // 17.satirdaki bakiye :100.0

        // bakiye miktarını 3katına cıkarın

        bakiye= bakiye*3;

        System.out.println("22. satirda bakiye  :"+ bakiye); // 22. satırda bakiye : 300.0



    }
}
